# while-loop

# while (kondisi = true):
#   aksi # bagian dari while
# aksi # bukan bagian dari while

angka = 0
print(f"angka awal -> {angka}")

# angka yang ada hentinya
while angka < 5:
    angka += 1
    print(f"angka sekarang -> {angka}")

# angka yang nggak ada hentinya
angka = 0
print(f"angka awal -> {angka}")

while angka < 5:
    print(f"angka sekarang -> {angka}")